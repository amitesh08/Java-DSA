package Forloop;

public class EvenOdd {
    public static void main(String[] args) {

        //wrp to show odd and even numbers till 10
        for(int a=1;a<=10;a++)
        {
            if (a%2==0)
                System.out.println("even "+a);
            else
                System.out.println("odd "+a);
        }
    }
}
